// Operadores lógicos

//* && => And -> E
console.log(true && true);
console.log(true && false);
console.log(false && false);
console.log();

//* || => Or -> Ou
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false); 
console.log();

//* ! => Not -> Não
console.log(!true);
console.log(!false);
console.log();
