//Operadores de comparação

//* >
console.log(10>5);

//* <
console.log(10<5);

//* >=
console.log(10>=5);

//* <=
console.log(10<=5);

//* ==
console.log(10==5);
console.log(10=='10'); //Funciona mas não deveria

//* !=
console.log(10!=5);

//* ===
console.log(10===5);
console.log(10==='10');

//* !==
console.log(10!==5);

